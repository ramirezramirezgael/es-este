class Trabajador  {
    nombre;
    apellido;
    horario;
    reparacion_realizada;
    experiencia_Lab;
    estudios;
    constructor(apodo,lastname,tiempo,arreglado,habilidad,formacion){
        this.nombre = apodo;
        this.apellido = lastname;
        this.horario = tiempo;
        this.reparacion_realizada = arreglado;
        this.experiencia_Lab = habilidad;
        this.estudios = formacion;
    }
}
